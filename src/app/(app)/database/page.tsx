export default async function DatabasePage() {
  const h = await headers();
  const userLevel = parseUserLevel(h.get("x-user-level"));

  // データ読み込み
  const { entities } = loadJson<{ entities: unknown[] }>("entities-data.json");
  const { incidents } = loadJson<{ incidents: unknown[] }>("area-incidents-data.json");
  const { missions } = loadJson<{ missions: unknown[] }>("mission-data.json");
  const { locations } = loadJson<{ locations: unknown[] }>("locations-data.json");

  let modules: unknown[] = [];
  let personnel: unknown[] = [];
  try {
    modules = loadJson<{ modules: unknown[] }>("modules-data.json").modules ?? [];
  } catch { /* データなし */ }
  try {
    personnel = loadJson<{ personnel: unknown[] }>("personnel-data.json").personnel;
  } catch { /* データなし */ }

  return (
    <DatabaseClient
      userLevel={userLevel}
      entities={entities as never}
      modules={modules as never}
      locations={locations as never}
      codexEntries={CODEX_ENTRIES as never}
      protocols={PROTOCOLS_CATALOG as never}
      personnel={personnel as never}
      stats={{
        missions: missions as never,
        entities: entities as never,
        personnel: personnel as never,
        incidents: incidents as never,
      }}
      incidents={incidents as never}
      missions={missions as never}
    />
  );
}

